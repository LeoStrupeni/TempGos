         <h1 class="text-center" style="color: gray;"> AVISO DE PRIVACIDAD</h1>
         <p>

  Pro Order S.A. de C.V., (el “Proveedor”), es responsable de la confidencialidad, uso y protección de la información personal que nos proporcione por los distintos medios que utilizamos para la difusión de los servicios que brindamos. Por lo que su información personal será utilizada, para fines de identificación en cualquier tipo de relación jurídica, recabando y usando sus datos personales para el cumplimiento de las siguientes finalidades:
</p> <p>
 <p>  •	Proveer al titular de los datos los servicios que ha solicitado.</p>
<p>  •	Confirmar su identidad e identificarlo.</p>
<p>  •	Otorgar certeza jurídica a los actos, hechos, y negocios jurídicos que celebre mediante instrumentos públicos o privados, así como para la redacción de esos instrumentos donde se hagan constar dichos actos o hechos.</p>
<p>  •	Brindarle asesoría atendiendo cada caso de manera concreta conforme a las necesidades de servicio y plataforma que se oferta</p>
<p>  •	Elaborar un expediente físico y/o electrónico.</p>
<p>  •	Procesar sus datos en nuestros sistemas internos.</p>
<p>
  De manera adicional, podríamos utilizar su información personal para realizar encuestas de evaluación del personal que labora para esta empresa y servicio, así como, para ofrecerle nuevos servicios o promociones. Si bien, estas finalidades no son necesarias para prestarle los servicios que solicita o contratar con nosotros, las mismas nos permiten brindarle un mejor servicio y elevar su calidad. En caso de que no desee que sus datos personales sean tratados para estos fines, desde este momento usted puede manifestar su negativa en esta Correduría, marcando el siguiente recuadro con una cruz y asentando su firma:

  No consiento que mis datos personales se utilicen para los fines:
</p>
<p>  •		La realización de encuestas de evaluación de los servicios prestados.</p>
<p>  •		El ofrecimiento de nuevos servicios o promociones.</p>
<p>
  La negativa para el uso de sus datos personales para estas finalidades no podrá ser un motivo para que se niegue el servicio o acceso a los programas, plataforma y actualizaciones. En caso de que no manifieste su negativa, se entenderá que autoriza el uso de su información personal para dichos fines.
  Para prestarle los servicios que se solicita requerimos por lo menos la siguiente información y/o documentación:
</p>
<p>  •		Identificación oficial con fotografía y firma.</p>
<p>  •		Nombre completo (incluyendo cualquier variante del mismo).</p>
<p>  •		Fecha y lugar de nacimiento.</p>
<p>  •		Ocupación actual.</p>
<p>  •		Estado Civil.</p>
<p>  •		Domicilio Actual.</p>
<p>  •		Teléfono.</p>
<p>  •		Correo electrónico.</p>
<p>  •		En caso de ser extranjero, el Documento Migratorio que acredite su legal estancia en el país.</p>
<p>  •		En caso de ser representante legal y/o apoderado de una persona física o jurídica, requerimos que presente el documento original del cual se desprenda el carácter con que comparece.</p>
<p>
  Dependiendo del servicio, plataforma o actualizaciones del programa proporcionado por el Proveedor, se le podrá solicitar mayor información y documentación, la cual será tratada de manera responsable y confidencial.
  Los datos personales que recaba el Proveeor, son obtenidos de los documentos programas y servicios solicitados por Usted y de los documentos privados u oficiales que nos proporciona directamente, así como, de los documentos que los diversos Registros y Dependencias Públicas Federales, Estatales y/o Municipales.

  De forma eventual, sus datos personales se comprometen para el cumplimiento de requerimientos legales o la atención de una orden fundada y motivada de las autoridades competentes en ejercicio de sus funciones de notificación, vigilancia y fiscalización, concretamente, con el Registro de la Propiedad y Comercio del Estado de Jalisco o de otras entidades federativas respecto a las operaciones traslativos de dominio, el Servicio de Administración Tributaria en materia de impuestos, así como autoridades judiciales o administrativas locales o federales en el marco de la legislación mexicana.
  Asimismo, le informamos que en caso de que esta Correduría se vea obligada o necesite transferir su información personal a terceros nacionales o extranjeros distintos de los mencionados en el párrafo anterior para el cumplimiento de nuestros servicios de Correduría, será informado previamente de esta situación a efecto de solicitarle autorización e infórmale sobre los destinatarios o terceros receptores y finalidades de dicha transferencia, en términos de lo previsto en los artículos 36 y 37 de la Ley Federal de Protección de Datos Personales en Posesión de los Particulares de su Reglamento.

  En cualquier caso, comunicaremos el presente aviso de privacidad a los destinatarios de sus datos personales, a fin de que respeten sus términos.

  Usted podrá acceder, rectificar, cancelar u oponerse (en lo sucesivo derechos ARCO) al tratamiento de sus datos personales que tenemos en nuestro registro y archivos, o bien, revocar su contenido para el uso de los mismos, presentando solicitud por escrito en nuestro domicilio dirigido al Represenante legal a dirección@proordersistem.com.mx

  Al respecto, queremos manifestarle que el ejercicio de los derechos ARCO o la revocación del consentimiento, es independiente de las modificaciones a los datos personales asentados en los instrumentos públicos  o privados que se expiden con motivo de la contratación de los servicios y programas que el Proveedor oferta, ,cualquier acción o petición que nos solicite respecto a los mismos se regirá por la legislación aplicable.

  Es importante informarle que su solicitud deberá de contener, el menos, la siguiente información: (i) su nombre o el de su representante, domicilio u otro medio para comunicarle la respuesta; (ii) los documentos que acrediten su identidad, o en caso, el de su representante; (iii) la descripción clara y precisa de los datos personales respecto de los cuales busca ejercer su derecho; y (iv) cualquier otro elemento o documento que facilite la localización de los datos personales. Asimismo, deberá de acreditar su identidad mediante identificación oficial vigente o en caso de presentar su solicitud a través de instrumento público.

  Nos comprometemos a darle respuesta en un plazo máximo de 20 (veinte) días hábiles contados a partir del día en que recibimos su solicitud, misma que pondremos a su disposición en nuestro domicilio, previa acreditación de su identidad. Si solicita acceso a sus datos personales, la producción de éstos se llevará a cabo a través de copias simple, archivos electrónicos, o bien, podrá consultarlos.

  Para el caso que se desee limitar el uso o divulgación de su información personal, ejercitar sus derechos de acceder, rectificar y cancelar sus datos personales, así como, de oponerse al tratamiento de los mismos y revocar el consentimiento que para tal fin nos haya otorgado, lo podrá realizar a través de los siguientes canales de atención:
</p>
